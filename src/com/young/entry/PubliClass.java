package com.young.entry;

public class PubliClass {
	private String taskNo;
	private String taskName;
	private String taskType;
	private String taskColledge;
	private String taskCredit;
	private String examTimes;
	private String score;
	private String taskPlace;

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

	public String getTaskNo() {
		return taskNo;
	}

	public void setTaskNo(String taskNo) {
		this.taskNo = taskNo;
	}

	public String getTaskName() {
		return taskName;
	}

	public void setTaskName(String taskName) {
		this.taskName = taskName;
	}

	public String getTaskType() {
		return taskType;
	}

	public void setTaskType(String taskType) {
		this.taskType = taskType;
	}

	public String getTaskColledge() {
		return taskColledge;
	}

	public void setTaskColledge(String taskColledge) {
		this.taskColledge = taskColledge;
	}

	public String getTaskCredit() {
		return taskCredit;
	}

	public void setTaskCredit(String taskCredit) {
		this.taskCredit = taskCredit;
	}

	public String getExamTimes() {
		return examTimes;
	}

	public void setExamTimes(String examTimes) {
		this.examTimes = examTimes;
	}

	public String getTaskPlace() {
		return taskPlace;
	}

	public void setTaskPlace(String taskPlace) {
		this.taskPlace = taskPlace;
	}

}
